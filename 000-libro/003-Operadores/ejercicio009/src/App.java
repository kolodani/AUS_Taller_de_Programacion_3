/*
 *  Ejercicio 09:
 *  Visualice los números más grande y más pequeño que se pueden representar con la notación ex
 *  ponencial en el tipo float y en el tipo double.
*/

public class App {
    public static void main(String[] args) throws Exception {
        System.out.println("El número más grande que se puede representar con la notación exponencial en el tipo float es: " + Float.MAX_VALUE);
        System.out.println("El número más pequeño que se puede representar con la notación exponencial en el tipo float es: " + Float.MIN_VALUE);
        System.out.println("El número más grande que se puede representar con la notación exponencial en el tipo double es: " + Double.MAX_VALUE);
        System.out.println("El número más pequeño que se puede representar con la notación exponencial en el tipo double es: " + Double.MIN_VALUE);
    }
}
